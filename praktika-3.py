def classify_number(s):
    """Возвращает 'целое', 'дробное' или 'не число'."""
    try:
        num = float(s)
        if num.is_integer():
            return "целое"
        else:
            return "дробное"
    except ValueError:
        return "не число"
# Ввод и вывод
userinput = input("Введите число: ")
result = classify_number(userinput)
print(f"Введённое значение — {result}.")
