# Запрашиваем у пользователя три целых числа
num1 = int(input("Введите первое число: "))
num2 = int(input("Введите второе число: "))
num3 = int(input("Введите третье число: "))
# Создаём список из трёх чисел
numbers = [num1, num2, num3]
# Инициализируем пустой список для чисел, попадающих в интервал [1, 3]
selected_numbers = []
# Проходим по каждому числу и проверяем, принадлежит ли оно интервалу [1, 3]
for num in numbers:
    if 1 <= num <= 3:
        selected_numbers.append(num)
# Выводим результат
if selected_numbers:
    print("Числа, принадлежащие интервалу [1, 3]:", selected_numbers)
else:
    print("Ни одно из чисел не принадлежит интервалу [1, 3].")

